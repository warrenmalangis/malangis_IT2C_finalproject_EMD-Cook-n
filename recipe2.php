<h3>Ingredients:</h3>

<ol class="ing">

  <li>15 pieces of kagang</li>
  <li>20 pieces of coconut apple (bingi)</li>
  <li>1 piece of coconut (niyog)</li>
  <li>3 pieces of onions</li>
  <li>2 cloves of garlic</li>
  <li>1 tablespoon of ground pepper pinch of salt</li>

</ol>
<br>
<br>
<h3>Procedure</h3>

<ol class="proc">

<?php

  echo "<li>Wash and brush the kagang with clean water.</li>";
  echo "<li>Open the crab shell and remove the food eaten by the crab (found inside the crab) and separate it in a bowl.</li>";
  echo "<li>Slice the onions and mince garlic.</li>";
  echo "<li>Chop the coconut apple (bingi).</li>";
  echo "<li>Grate the coconut and get the coconut milk.</li>";
  echo "<li>Mix the chopped coconut apple (bingi), fats of the crab, slice onions, mince garlic, salt and the ground pepper. Then mix it thorouhly.</li>";
  echo "<li>Open each of the crab shell (stomach part) and put little amount of the mixed ingredients.</li>";
  echo "<li>Wrap a little amount of mixed ingredients in the banana leaf.</li>";
  echo "<li>Then place the crab with the wrapped ingredients in a casserole.</li>";
  echo "<li>Place the casserole in the stove and add coconut milk.</li>";
  echo "<li>Wait until it boils and when the crab is already color red, then it is cooked already.</li>";
  echo "<li>Remove the casserole from the stove, let it cool the serve.</li>";

  ?>

