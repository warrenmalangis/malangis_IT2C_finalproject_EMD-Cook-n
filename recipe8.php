<h3>Ingredients:</h3>

<ol class="ing">

  <li>1 ½ lbs chicken cut into serving pieces</li>
  <li>2 cups coconut milk</li>
  <li>1 piece Knorr Chicken Cube</li>
  <li>½ cup white vinegar</li>
  <li>¼ cup soy sauce</li>
  <li>6 cloves garlic</li>
  <li>5 pieces bay leaves dried</li>
  <li>2 teaspoons whole peppercorn</li>
  <li>3 tablespoons cooking oil</li>

</ol>
<br>
<br>
<h3>Procedure</h3>

<ol class="proc">

    <?php

  echo "<li>Heat oil in a pan.</li>";
  echo "<li>Crush the garlic, and then saute until it starts to brown.</li>";
  echo "<li>Fry the chicken for 1 to 1 ½ minutes per side.</li>";
  echo "<li>Pour soy sauce and vinegar. Cover the pan. Let boil. Flip the chicken pieces afterwards. Continue cooking for 5 minutes.</li>";
  echo "<li>Add whole peppercorn and bay leaves. Pour coconut milk. Stir and then let the mixture boil.</li>";
  echo "<li>Add Knorr Chicken Cube. Stir and cover the pan. Adjust heat to low. Cook for 20 minutes.</li>";
  echo "<li>Remove the cover and continue to cook until sauce reduces to desired amount.</li>";
  echo "<li>Serve with warm rice. Share and enjoy!</li>";

    ?>

</ol>
