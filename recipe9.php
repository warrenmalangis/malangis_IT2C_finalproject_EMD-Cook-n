<h3>Ingredients:</h3>

<ol class="ing">

  <li>2 cups purple tinted glutinous rice</li>
  <li>1 tsp. salt</li>
  <li>2 cups fresh coconut milk</li>
  <li>water for boiling</li>
  <li>1 cup sugar (brown or white)</li>
  <li>coconut leaves for wrapping</li>

</ol>
<br>
<br>
<h3>Procedure</h3>

<ol class="proc">

    <?php

  echo "<li>Rinse the rice until water runs clear then drain.</li>";
  echo "<li>In a wok or heavy pot, combine rice, coconut milk, sugar and salt.</li>";
  echo "<li>Bring to a boil. Simmer for about 30 minutes in medium heat while stirring constantly then reduce to lower heat.</li>";
  echo "<li>Mixture is done when rice becomes very sticky and almost dry.</li>";
  echo "<li>Let the temperature cool down before wrapping.</li>";
  echo "<li>Prepare the coconut leaves by wiping away any white residues with a damp cloth.</li>";
  echo "<li>Remove the spine and trim (size preference).</li>";
  echo "<li>Scoop the rice mixture and place it over the prepared coconut leaves.</li>";
  echo "<li>Start closing the leaves by bending and closing the gaps through a sack tie.</li>";
  echo "<li>Repeat until rice mixture is finished. Set up the steamer.</li>";
  echo "<li>Arrange the wrapped mixture into the pot. Single layer or on top of each other.</li>";
  echo "<li>Steam over medium heat for about 1 hour.</li>";
  echo "<li>Remove from the pot. Serve with kalamay hati. Enjoy!</li>";

    ?>

</ol>
