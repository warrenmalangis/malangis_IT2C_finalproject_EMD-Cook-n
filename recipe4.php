<h3>Ingredients:</h3>

<ol class="ing">

  <li>2 cups glutinous rice sweet rice or malagkit, washed</li>
  <li>2 1/4 cups brown sugar</li>
  <li>4 cups coconut milk</li>
  <li>2 cups coconut cream</li>
  <li>1/2 tsp salt</li>
  <li>banana leaves</li>

</ol>
<br>
<br>
<h3>Procedure</h3>

<ol class="proc">

    <?php

  echo "<li>Combine glutinous rice and 4 cups of coconut milk in a cooking pot. Heat and let boil, then reduce heat and cook while stirring until the texture thickens. This process can range from 18 to 20 minutes, or even more.</li>";
  echo "<li>Add 1 1/4 cups of sugar and salt. Continuously stir and cook for 10 more minutes or until the rice is completely done. The texture should be very sticky.</li>";
  echo "<li>Meanwhile, arrange banana leaves in a 9 by 13 inches baking pan. Transfer the cooked sticky rice to the baking pan. Set aside.</li>";
  echo "<li>Make the topping by heating a sauce pan and combining 2 cups of coconut cream and remaining 1 cup of brown sugar.Let boil and simmer while stirring once in a while until the texture thickens.</li>";
  echo "<li>Preheat oven to 400 degrees Fahrenheit.</li>";
  echo "<li>Spread the topping equally over the cooked sticky rice. Bake for 12 to 18 minutes or until the topping becomes a little firm.</li>";
  echo "<li>Remove from the oven and let cool.</li>";
  echo "<li>Serve. Share and enjoy</li>";

    ?>

</ol>
