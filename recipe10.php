<h3>Ingredients:</h3>

<h2>For Rice Cake</h2>

<ol class="ing">

  <li>6 cups coconut milk</li>
  <li>3 cups glutinous rice, washed</li>
  <li>1 1/2cup brown sugar</li>
  <li>1 tsp salt</li>
  <li>Banana Leaves</li>

</ol>
<br>
<br>

<h2>For Toppings</h2>

<ol class="ing">

  <li>2 1/2 cups Coconut Cream (Latik)</li>
  <li>1 cup Brown Sugar</li>
  <li>½ tsp salt</li>

</ol>
<br>
<br>
<h3>Procedure</h3>

<ol class="proc">

    <?php

  echo "<li>In a pot combine glutinous rice and 6 cups of coconut milk and bring to a boil then reduce heat to low.</li>";
  echo "<li>Simmer gently while occasionally mixing to cook them evenly and not burn the bottom, let the mixture thicken until it’s nearly dried out.</li>";
  echo "<li>Add sugar and salt then mix continuously on medium-low heat for 10 minutes.</li>";
  echo "<li>At this stage, it will be really sticky.</li>";
  echo "<li>Line a 9x 13 baking tray with banana leaf then transfer the cooked glutinous rice. Set it aside.</li>";
  echo "<li>Prepare your topping by mixing all topping ingredients together in a pan, bring it to a boil and simmer on medium heat uncovered until heavily reduced but still runny.</li>";
  echo "<li>Pour topping over the rice cake on the baking pan then bake in a 180C preheated oven for 20 minutes or until topping becomes really thick and dark (not burnt).</li>";
  echo "<li>Remove from oven and set it aside to cool down.</li>";
  echo "<li>Serve!</li>";

    ?>

</ol>
